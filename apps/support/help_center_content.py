def get_help_center_content(request) -> dict:
    payload = deepcopy(HELP_CENTER_CONTENT)
    for document in payload["documents"]:
        meta = LEGAL_DOCUMENTS_BY_SLUG[document["id"]]
        document["pdf_file_name"] = meta["file_name"]
        document["pdf_download_url"] = request.build_absolute_uri(
            reverse(
                "support-legal-document-download",
                kwargs={"slug": document["id"]},
            )
        )
    return payload


def get_help_document(slug: str) -> dict | None:
    for document in HELP_CENTER_CONTENT["documents"]:
        if document["id"] == slug:
            result = deepcopy(document)
            result["pdf_file_name"] = LEGAL_DOCUMENTS_BY_SLUG[slug]["file_name"]
            return result
    return None
